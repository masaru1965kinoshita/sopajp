<div class="suv_t">
  <h2>教育プログラムへの想い</h2>
   <p>一方で、今のこどもたちが大人になったときの世界はどうなっているのか？！<br>今のままの経済社会であれば資源は枯渇してしまうかもしれない…<br>さらに技術の進歩は目覚ましくコンピュータが人間を超える時代がやってくるかもしれない…</p>
   <p>こどもたちの学力の基礎を築くために、国の指導要領をこどもたちに伝えることはもちろん大切です。<br>またそれらの他に、環境や情報など新しい分野について早い時期に知見・体感できることも、将来を担うこどもたちには求められています。<br>しかしながら、現場の教育関係者でもこれら分野の必要性を感じている方も多い中、「導入費用のハードルが高い」「ITや環境の専門性が必要」「そもそも取り組む時間が取れない」というような様々な課題があります。</p>
   <p>そこで私たちは、こどもたちにタブレットを贈り、こどもたちが環境やICTを楽しく学ぶことができる教育プログラムを無償で提供し続たい、との思いから『リユース for きっず』スタートしました。</p>
   <p>――現場の教育関係者の悩みを解決し、そして何より、未来を担うこどもたちへの貴重な体験を贈りたい。</p>
