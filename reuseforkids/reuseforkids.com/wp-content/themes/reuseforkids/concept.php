   <h2>リユースに取り組む大切さ</h2>
   <p>世界的な人口爆発と経済発展により、現在暮らしている世代のニーズを満たす為、地球環境を破壊し資源を使い続けることに物理的また、生態学的な限界がきています。その為、日本でも 国・地方公共団体・事業者など、国民一人一人が責任を持って、循環型社会をつくっていくことを目指す 「循環型社会形成推進基本法」 を制定しています。その中には、資源の循環的な利用及び、処分の基本原則（第7条)として、廃棄やリサイクルよりもまずリユースを優先する事が掲げられています。</p>
   <p>『リユース for きっず』の活動は、企業・ＮＰＯ・地域が連携した新しいリユースのかたちが評価され、環境省の使用済製品等のリユース促進事業研究会において、平成27年度モデル事業として採択されました。</p>
